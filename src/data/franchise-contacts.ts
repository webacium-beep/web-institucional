function buildWhatsAppLink(dialCode: string, phoneNumber: string) {
  const normalizedDialCode = dialCode.replace(/\D/g, '');
  const normalizedPhoneNumber = phoneNumber.replace(/\D/g, '');

  return `https://wa.me/${normalizedDialCode}${normalizedPhoneNumber}`;
}

